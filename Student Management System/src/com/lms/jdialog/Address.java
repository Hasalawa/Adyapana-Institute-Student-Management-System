    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        try {

            String line1 = jTextField1.getText();
            String line2 = jTextField2.getText();
            String city = String.valueOf(jComboBox1.getSelectedItem());
            String district = String.valueOf(jComboBox2.getSelectedItem());
            String province = String.valueOf(jComboBox3.getSelectedItem());

            if (line1.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter address line1", "Warnning", JOptionPane.WARNING_MESSAGE);
            } else if (line2.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter address line2", "Warnning", JOptionPane.WARNING_MESSAGE);
            } else if (city.equals("Select")) {
                JOptionPane.showMessageDialog(this, "Please select a city", "Warnning", JOptionPane.WARNING_MESSAGE);
            } else if (district.equals("Select")) {
                JOptionPane.showMessageDialog(this, "Please select a district", "Warnning", JOptionPane.WARNING_MESSAGE);
            } else if (province.equals("Select")) {
                JOptionPane.showMessageDialog(this, "Please select a province", "Warnning", JOptionPane.WARNING_MESSAGE);
            } else {

                MySQL.executeIUD("INSERT INTO `address` (`line1`,`line2`,`city_id`) VALUES "
                        + "('" + line1 + "','" + line2 + "','" + cityMap.get(city) + "')");

                MySQL.executeIUD("UPDATE `city` SET `district_id` = '" + districtMap.get(district) + "' WHERE "
                        + "`id` = '" + cityMap.get(city) + "' ");

                MySQL.executeIUD("UPDATE `district` SET `province_id` = '" + provinceMap.get(province) + "' WHERE "
                        + "`id` = '" + districtMap.get(district) + "'");

                loadAddress(jTextField3.getText());
                reSet();
                JOptionPane.showMessageDialog(this, "Address added successfull!",
                        "Information", JOptionPane.INFORMATION_MESSAGE);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton3ActionPerformed

