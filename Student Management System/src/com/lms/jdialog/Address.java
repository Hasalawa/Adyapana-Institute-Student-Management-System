    public void deleteButton() {

        TableDeleteEvent event = new TableDeleteEvent() {
            @Override
            public void onDelete(int row) {
                String id = String.valueOf(jTable1.getValueAt(row, 0));
                try {

                    ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `teacher` WHERE `address_id` = '" + id + "'");

                    if (resultSet.next()) {
                        JOptionPane.showMessageDialog(null, "Address cannot be deleted as it is referenced in the some table.", "Error",
                                JOptionPane.ERROR_MESSAGE);
                    } else {

                        int response = JOptionPane.showConfirmDialog(null, "Do you want to delete?",
                                "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

                        if (response == JOptionPane.YES_OPTION) {
                            MySQL.executeIUD("DELETE FROM `address` WHERE `id` = '" + id + "'");
                            loadAddress(jTextField3.getText());
                            JOptionPane.showMessageDialog(null, "Record deleted successfully.",
                                    "Success", JOptionPane.INFORMATION_MESSAGE);
                        }

                    }

                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        };

        jTable1.getColumnModel().getColumn(6).setCellRenderer(new TableDeleteCellRender());
        jTable1.getColumnModel().getColumn(6).setCellEditor(new TableDeleteCellEditor(event));

    }

