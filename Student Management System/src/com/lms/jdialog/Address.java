    private void loadCity() {

        try {

            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `city` ORDER BY `name` ASC");

            Vector<String> vector = new Vector<>();
            vector.add("Select");

            while (resultSet.next()) {
                vector.add(resultSet.getString("name"));
                cityMap.put(resultSet.getString("name"), resultSet.getString("id"));
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            jComboBox1.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void loadAddress(String id) {

        try {

            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `address` INNER  JOIN `city`"
                    + "ON `address`.`city_id` = `city`.`id` INNER JOIN `district` ON `city`.`district_id` = `district`.`id` "
                    + "INNER JOIN `province` ON `district`.`province_id` = `province`.`id` WHERE `address`.`id` LIKE '" + id + "%'");

            DefaultTableModel defaultTableModel = (DefaultTableModel) jTable1.getModel();
            defaultTableModel.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("id"));
                vector.add(resultSet.getString("line1"));
                vector.add(resultSet.getString("line2"));
                vector.add(resultSet.getString("city.name"));
                vector.add(resultSet.getString("district.name"));
                vector.add(resultSet.getString("province.name"));

                defaultTableModel.addRow(vector);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

