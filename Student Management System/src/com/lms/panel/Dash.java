    private void loadIncom() {
        try {

            ResultSet resultSet = MySQL.executeSearch("SELECT SUM(class_fee) FROM `invoice` INNER JOIN `student_has_class` ON "
                    + "`invoice`.`student_has_class_id` = `student_has_class`.`id` INNER JOIN `month` ON "
                    + "`invoice`.`month_id` = `month`.`id` INNER JOIN `student` ON "
                    + "`invoice`.`student_sNo` = `student`.`sNo` INNER JOIN `class` ON "
                    + "`student_has_class`.`class_class_no` = `class`.`class_no` INNER JOIN `subject` ON "
                    + "`class`.`subject_id` = `subject`.`id`");


            if (resultSet.next()) {
                jLabel9.setText(resultSet.getString(1)+"0");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

