    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int row = jTable1.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select student detail table row.",
                    "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            String sNo = String.valueOf(jTable1.getValueAt(row, 0));
            String fname = String.valueOf(jTable1.getValueAt(row, 1));
            String lname = String.valueOf(jTable1.getValueAt(row, 2));
            String fullName = fname + " " + lname;
            StuEnrollForSub sefs = new StuEnrollForSub(dashboard, true, sNo, fullName);
            sefs.setVisible(true);

        }

    }//GEN-LAST:event_jButton1ActionPerformed

